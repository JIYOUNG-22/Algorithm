SELECT
    FO.PRODUCT_ID,
    FP.PRODUCT_NAME,
    SUM(FO.AMOUNT) * FP.PRICE AS TOTAL_SALES
FROM
    FOOD_ORDER FO
JOIN
    FOOD_PRODUCT FP
    ON FO.PRODUCT_ID = FP.PRODUCT_ID
WHERE
    DATE_FORMAT(FO.PRODUCE_DATE, '%Y-%m') = '2022-05'
GROUP BY
    FO.PRODUCT_ID, FP.PRODUCT_NAME, FP.PRICE
ORDER BY
    TOTAL_SALES DESC,
    FO.PRODUCT_ID ASC;
